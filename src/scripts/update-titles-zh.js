// --- Main ---

async function main() {
  const client = new MongoClient(config.mongo.uri);
  try {
    await client.connect();
    const db = client.db(config.mongo.dbName);
    const col = db.collection('targeted_results');

    const entries = Object.entries(TRANSLATIONS);
    console.log(`Loaded ${entries.length} translations to apply.`);

    // Use bulkWrite for efficiency
    const ops = entries.map(([id, zh]) => ({
      updateMany: {
        filter: { listing_id: id },
        update: { $set: { title_zh: zh } },
      },
    }));

    const result = await col.bulkWrite(ops, { ordered: false });
    console.log(`Done. Matched: ${result.matchedCount}, Modified: ${result.modifiedCount}`);

    // Check for any remaining untranslated
    const remaining = await col.countDocuments({ $expr: { $eq: ['$title_zh', '$title'] } });
    console.log(`Remaining untranslated: ${remaining}`);
  } finally {
    await client.close();
  }
}

main().catch((err) => {
  console.error('Fatal error:', err);
  process.exit(1);
});
